void Number(int iNo)
{
    int iCnt = 0;

    if(iNo <= 50)
    {
        printf("Number is Small");
    }
    else if(iNo < 50 && iNo >= 100)
    {
        printf("Number is Medium");
    }
    else
    {
        printf("Number is Large");
    }
}

int main()
{
    int iValue = 0;

    printf("Enter Number : ");
    scanf("%d", &iValue);

    Number(iValue);

    return 0;
}

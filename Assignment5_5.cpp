#include<iostream>
using namespace std;

////////////////////////////////////////////////////////////////////////////////////////////
//  
//  Function name : MultipleDisplay
//  Input :         int
//  Output :        void
//  Description :   write a program which accept number from user and print 1st 5 multiples of that number
//  language :      C++
//  Date :          27/05/2026
//  Author :        Srushti Nitin Pagar
//
////////////////////////////////////////////////////////////////////////////////////////////

void MultipleDisplay(int iNo)
{
    int iCnt = 0;

    for(iCnt = 1; iCnt <= 5; iCnt++)
    {
        cout<<iNo * iCnt <<endl; 
    }
}

int main()
{
    int iValue = 0;

    cout<<"Enter Number : ";
    cin>>iValue;

    MultipleDisplay(iValue);

    return 0;
}

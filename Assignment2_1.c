void Display(int iNo)
{
    int iCnt = 0;
    //Write Updater

    while(iCnt < iNo)
    {
        printf("*");
        iCnt++;
    }
}

//////////////////////////////////////////////////////////////////////////////////////////////////
//
// Application to Accept number from user and print that number of * on screen
//
/////////////////////////////////////////////////////////////////////////////////////////////////
int main()
{
    int iValue = 0;

    printf("How many numbers of * you want to print? ");
    scanf("%d" , &iValue);

    Display(iValue);

    return 0 ;
}

void Display(int iNo)
{
    int iCnt = 0;
    
    for(iCnt = 1; iCnt <= iNo; iCnt++)
    {
        cout<<iCnt<<" ";
    }
}

int main()
{
    int iValue = 0;

    cout<<"Enter Number of Elements to Print : ";
    cin>>iValue;

    Display(iValue);

    return 0;
}

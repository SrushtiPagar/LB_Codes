#include<stdio.h>

//////////////////////////////////////////////////////////////////////////////////////////////
//
// Function name : Accept
// Input :         int
// Output :        void
// Description :   Program to Accept number from user and print that number of * on screen
// language :      c
// Date :          21/05/2026
// Author :        Srushti Nitin Pagar
//
//////////////////////////////////////////////////////////////////////////////////////////////

void Accept(int iNo)
{
    int iCnt = 0;

    for(iCnt = 0 ;  iCnt < iNo ; iCnt++)
    {
        printf("*");
    }
}

int main()
{
    int iValue = 0;
    iValue = 5;

    Accept(iValue);

    return 0 ;
}

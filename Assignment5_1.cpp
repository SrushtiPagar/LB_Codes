#include<iostream>
using namespace std;

////////////////////////////////////////////////////////////////////////////////////////////
//  
//  Function name : Pattern
//  Input :         int
//  Output :        void
//  Description :   write a program which accept number from user and print that number of $ and * on screen
//  language :      C++
//  Date :          27/05/2026
//  Author :        Srushti Nitin Pagar
//
////////////////////////////////////////////////////////////////////////////////////////////

void Pattern(int iNo)
{
    int iCnt = 0;
    
    for(iCnt = 1; iCnt <= iNo; iCnt++)
    {
        cout<<" $ * ";
    }
}

/////////////////////////////////////////////////////////////////////////////////////////////
//
//  Application to Print $ and * on Screen
//
/////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    cout<<"Enter Number of $ and * to Print : ";
    cin>>iValue;

    Pattern(iValue);

    return 0;
}

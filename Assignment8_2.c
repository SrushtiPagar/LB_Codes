double RectangleArea(float fWidth, float fHeight)                                      
{
    double dArea;

    dArea = fWidth * fHeight;
    
    return dArea;
}

//////////////////////////////////////////////////////////////////////////////////////////
// 
// Write a program which accepts width and height of rectangle from user and calculates its area.
//
///////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    float fWidth = 0.0f;
    float fHeight = 0.0f;
    double dRet = 0.0;

    printf("Enter width of rectangle : ");
    scanf("%f", &fWidth);
    printf("Enter height of rectangle : ");
    scanf("%f", &fHeight);
    
    dRet = RectangleArea(fWidth, fHeight);
    
    printf("Area of rectangle is : %lf\n", dRet);

    return 0;
}

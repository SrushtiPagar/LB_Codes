#include<stdio.h>

//////////////////////////////////////////////////////////////////////////////////////////////
//
// Function name : Display
// Input :         int
// Output :        void
// Description :   Program to Display "Hello" if the number is less than 10 and "Demo" if the number is greater than 10
// language :      c
// Date :          21/05/2026
// Author :        Srushti Nitin Pagar
//
//////////////////////////////////////////////////////////////////////////////////////////////

void Display(int iNo)
{
    if(iNo < 10 )
    {
        printf("Hello");
    }
    else
    {
        printf("Demo");
    }
}

int main()
{
    int iValue = 0;

    printf("Enter a Number : ");
    scanf("%d" , &iValue);

    Display(iValue);

    return 0 ;
}

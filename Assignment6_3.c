int Factorial(int iNo)
{
    int iCnt = 0;
    int iFact = 1;

    if(iNo < 0)
    {
        iNo = -iNo;
    }

    for(iCnt = iNo; iCnt > 0; iCnt--) 
    { 
        iFact = iFact * iCnt; 
    }
    
    return iFact;
}

int main()
{
    int iValue = 0;
    int iRet = 0;

    printf("Enter Number to find its factorial : ");
    scanf("%d", &iValue);

    iRet = Factorial(iValue);

    printf("Factorial of %d is %d", iValue, iRet);

    return 0;
}

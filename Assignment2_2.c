//////////////////////////////////////////////////////////////////////////////////////////////

void Display(int iNo)
{
    //Write Updater
    while(iNo > 0)
    {
        printf(" * ");
        iNo--;
    }
}

int main()
{
    int iValue = 0;

    printf("How many numbers of * you want to print? ");
    scanf("%d" , &iValue);

    Display(iValue);

    return 0 ;
}

#include<stdio.h>

//////////////////////////////////////////////////////////////////////////////////////////////
//
// Function name : FhToCs
// Input :         float
// Output :        double
// Description :   Write a program which accepts temperature in Fahrenheit and converts it into Celsius.
// language :      c
// Date :          28/05/2026
// Author :        Srushti Nitin Pagar
//
//////////////////////////////////////////////////////////////////////////////////////////////

double FhToCs(float fTemp)                                      
{
    double dTemp;

    dTemp = (fTemp - 32) * 5/9;
    
    return dTemp;
}

//////////////////////////////////////////////////////////////////////////////////////////
// 
// Write a program which accepts temperature in Fahrenheit and converts it into Celsius.
//
///////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    float fValue = 0.0f;
    double dRet = 0.0;

    printf("Enter temperature in Fahrenheit : ");
    scanf("%f", &fValue);

    dRet = FhToCs(fValue);

    printf("Temperature in Celsius is : %.2lf\n", dRet);

    return 0;
}
